#WHILE LOOP
#print i as long as i <= 6 using while loop
print("Printing i as long as i <= 6 using while loop:")
i = 1
while i <= 6:
  print(i)
  i = i + 1

print(" ")
#FOR LOOP
#printing i as long as i <= 6 using for loop
print("printing i as long as i <= 6 using for loop:")
i = 1 
for i in range(6):  #The range() function returns a sequence of numbers, 
  #starting from 0 by default, and increments by 1 (by default), and ends at a specified number.
  print(i)
